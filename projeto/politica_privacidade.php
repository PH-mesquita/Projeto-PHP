    </ul>
    
    <h3>Segurança</h3>
    <p>Estamos comprometidos em garantir que suas informações estejam seguras. Para evitar acesso não autorizado ou divulgação, implementamos procedimentos físicos, eletrônicos e administrativos adequados para proteger e proteger as informações que coletamos online.</p>
    
    <h3>Como Usamos Cookies</h3>
    <p>Um cookie é um pequeno arquivo que pede permissão para ser colocado no disco rígido do seu computador. Uma vez que você concorda, o arquivo é adicionado e o cookie ajuda a analisar o tráfego da web ou informa quando você visita um site específico. Usamos cookies de registro de tráfego para identificar quais páginas estão sendo usadas. Isso nos ajuda a analisar dados sobre o tráfego da página da web e melhorar nosso site para adaptá-lo às necessidades do cliente. Usamos essas informações apenas para fins de análise estatística e, em seguida, os dados são removidos do sistema.</p>
    
    <h3>Links para Outros Sites</h3>
    <p>Nosso site pode conter links para outros sites de interesse. No entanto, uma vez que você tenha usado esses links para sair do nosso site, você deve notar que não temos controle sobre esse outro site. Portanto, não podemos ser responsáveis pela proteção e privacidade de qualquer informação que você fornecer enquanto visita esses sites e esses sites não são regidos por esta declaração de privacidade. Você deve ter cuidado e olhar para a declaração de privacidade aplicável ao site em questão.</p>
    
